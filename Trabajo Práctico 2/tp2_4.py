pi=3.14159

radio=float(input("Ingrese la longitud del radio de la circunferencia: "))

#La superficie del círculo
print("La superficie del círculo es: %.2f"% (pi*(radio**2)), "cm")

#El perímetro de la circunferencia
print("El perímetro de la circunferencia es: %.2f"% (pi*2*radio), "cm")

#La superficie de la esfera
print("La superficie de la esfera es: %.2f"% (4*pi*(radio**2)), "cm2")

#El volumen de la esfera
print("El volumen de la esfera es: %.2f"% ((4/3)*pi*(radio**3)), "cm3")



